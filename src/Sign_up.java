    private void SignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignupActionPerformed
        //회원가입 버튼 누르면 파일로 아이디 비번 이름 저장하기
        String id = ID.getText();
        String password = PW.getText();
        String pwcheck = PW_Check.getText();
        String name = Name.getText();
        
        BufferedWriter writer = null;
        
        try{
            if((id.equals("")||password.equals(""))||(pwcheck.equals("")||name.equals(""))){
                JOptionPane.showMessageDialog(this,"정확히 입력해주세요");
                throw new IOException();
            }else if(isDuplicate(id)){
                JOptionPane.showMessageDialog(this,"중복된 아이디가 있습니다.");
                throw new IOException();
            }else if(!password.equals(pwcheck)){
                JOptionPane.showMessageDialog(this,"비밀번호가 틀렸습니다.");
                throw new IOException();
            }
            writer = new BufferedWriter(new FileWriter("C:\\Users\\NSH\\Documents\\user.txt", true));
            idcode(Icode);
            writer.write(id+" "+password+" "+name+" "+Icode);
            writer.newLine();
            writer.close();
            JOptionPane.showMessageDialog(this,"회원가입 완료.");
            setVisible(false);
            new Login().setVisible(true);           
            
        }catch(IOException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "회원가입 안됨");
        }
    }//GEN-LAST:event_SignupActionPerformed

