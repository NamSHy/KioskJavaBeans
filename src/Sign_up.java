    private boolean isDuplicate(String id){     //중복된 아이디가 있는지 확인하는 메소드
        try(BufferedReader reader = new BufferedReader(new FileReader("C:\\Users\\NSH\\Documents\\user.txt"))){//파일에서 가져오기
            String line;
            while((line = reader.readLine()) != null){ //텍스트 파일 null이 될때까지 전부 읽기
                String[] userInfo = line.split(" ");//userInfo배열 만들어서 띄어쓰기별로 배열 인덱스 부여
                if(userInfo.length >= 1 && userInfo[0].equals(id)){
                    return true;    //중복된 ID 존재
                }                
            }
            
        }catch(IOException ex){
            ex.printStackTrace();
        }
        return false;//중복된 id가 없음
    }
    
    private void idcode(int iden){
        try(BufferedReader reader = new BufferedReader(new FileReader("C:\\Users\\NSH\\Documents\\user.txt"))){//파일에서 가져오기
            String line;
            while((line = reader.readLine()) != null){ //텍스트 파일 null이 될때까지 전부 읽기
                String[] userInfo = line.split(" ");//userInfo배열 만들어서 띄어쓰기별로 배열 인덱스 부여
                if(userInfo.length >= 1){
                    if(!userInfo[3].equals(iden)){
                        Icode = Integer.parseInt(userInfo[3]);
                        Icode++;
                    }
                }                
            }
        }catch(IOException ex){
            ex.printStackTrace();
        }
    }
    
